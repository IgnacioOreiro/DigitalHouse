console.log('Operadores de comparacion SIMPLES(Booleans)')

// Valor

console.log('El resultado de esta opreacion es = ' + (5 == '5') + '\n') // true
console.log('El resultado de esta opreacion es = ' + (5 == 6) + '\n') // false

console.log('Operadores de comparacion COMPELJOS (Booleans)')

// Valor y tipo de dato

console.log('El resultado de esta opreacion es = ' + (5 == 5) + '\n') // true
console.log('El resultado de esta opreacion es = ' + (5 === '5') + '\n') // false

// Mayor que

console.log('El resultado de esta opreacion es = ' + (5 > 4) + '\n') // true
console.log('El resultado de esta opreacion es = ' + (5 < 4) + '\n') // false


// Desigualdad (!)

// Simple
console.log('El resultado de esta opreacion es = ' + (5 != 5) + '\n') // true!(false)

// Estricta
console.log('El resultado de esta opreacion es = ' + (5 !== 4) + '\n') // false!(true)


